export function getOptionsFromTasks(tasks: Task[]) {
  const workOptions = [...new Set(tasks.map((t) => t.work))].map((w) => ({
    label: w,
    value: w,
  }));

  const unitOptions = [...new Set(tasks.map((t) => t.unit))].map((u) => ({
    label: u,
    value: u,
  }));

  return { workOptions, unitOptions };
}
